    private void registerJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerJButtonActionPerformed
        // TODO add your handling code here:
         
        try{
                if(nameTextField.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Name cannot be empty", "Warning", JOptionPane.WARNING_MESSAGE);
                }
 
//                else if(dobField.getDate()){
//                JOptionPane.showMessageDialog(null, "Date of Birth cannot be empty", "Warning", JOptionPane.WARNING_MESSAGE);
//                }
                
                else if(genderComboBox.getSelectedItem().equals("Select")){
                JOptionPane.showMessageDialog(null, "Choose a Gender", "Warning", JOptionPane.WARNING_MESSAGE);
                }
          
                else if(addressTextField.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Address cannot be empty", "Warning", JOptionPane.WARNING_MESSAGE);
                }
                
                else if(contactTextField.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Contact Number cannot be empty", "Warning", JOptionPane.WARNING_MESSAGE);
                }
                
                else if(emailTextField.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "E-mail cannot be empty", "Warning", JOptionPane.WARNING_MESSAGE);
                }
                
                else{
                
                    String name = nameTextField.getText();
                    String email = emailTextField.getText();
                    Date dob = dobField.getDate();

                    String cont = contactTextField.getText();
                    String add = addressTextField.getText();
                    String gender = genderComboBox.getSelectedItem().toString();

                    Patient p = new Patient();
                    p.setName(name);
                    p.setEmail(email);
                    p.setDateOfBirth(dob);
                    p.setContactNo(cont);
                    p.setAddress(add);
                    p.setGender(gender);
        
            //      PatientOrganisation po = new PatientOrganisation();
                    Patient p1 = org.getPatientDirectory().createPatient(p);
            //      System.out.println(p1);
        
                
                JOptionPane.showMessageDialog(null, "Patient Details added Successfully");
                
                    //reset fields
                    nameTextField.setText("");
                    emailTextField.setText("");
                    contactTextField.setText("");
                    addressTextField.setText("");
                    genderComboBox.setSelectedIndex(0);
                    
                    
                    }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, "Invalid Data! Please enter valid data.", "Warning", JOptionPane.WARNING_MESSAGE);
            }
       
        
    }//GEN-LAST:event_registerJButtonActionPerformed

