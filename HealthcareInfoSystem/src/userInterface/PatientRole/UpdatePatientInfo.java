    public void populatePatientDetails(){

       
       for (Patient patient: org.getPatientDirectory().getPatientDirectory()){
           if(userAccount.getEmployee().getName().equalsIgnoreCase(patient.getName())){
               nameTextField.setText(patient.getName());
               dobField.setDate(patient.getDateOfBirth());
               contactTextField.setText(patient.getContactNo());
               addressTextField.setText(patient.getAddress());
               genderComboBox.setSelectedItem(patient.getGender());
               emailTextField.setText(patient.getEmail());
           }
           
       }
       
       
    }

